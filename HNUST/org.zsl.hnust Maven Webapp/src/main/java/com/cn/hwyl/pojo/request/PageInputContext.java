package com.cn.hwyl.pojo.request;

public class PageInputContext {

	
	private int	currPageIndex;//	当前页码
	private int	recordNumPer;//	每页最大记录数
	
	public int getCurrPageIndex() {
		return currPageIndex;
	}
	public void setCurrPageIndex(int currPageIndex) {
		this.currPageIndex = recordNumPer *(currPageIndex-1);
	}
	public int getRecordNumPer() {
		return recordNumPer;
	}
	public void setRecordNumPer(int recordNumPer) {
		this.recordNumPer = recordNumPer;
	}
	
	

}
