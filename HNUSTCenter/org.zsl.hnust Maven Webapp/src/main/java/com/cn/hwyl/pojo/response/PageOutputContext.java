package com.cn.hwyl.pojo.response;

import com.cn.hwyl.pojo.request.PageInputContext;

public class PageOutputContext {

	private int	recordNum;//总记录数
	private int	pageNum;//	页数
	private PageInputContext pageInputContext;//	页输入对象
	public int getRecordNum() {
		return recordNum;
	}
	public void setRecordNum(int recordNum) {
		this.recordNum = recordNum;
	}
	public int getPageNum() {
		return pageNum;
	}
	public void setPageNum(int pageNum) {
		this.pageNum = pageNum;
	}
	public PageInputContext getPageInputContext() {
		return pageInputContext;
	}
	public void setPageInputContext(PageInputContext pageInputContext) {
		this.pageInputContext = pageInputContext;
	}
	
	

}
